DROP TABLE BETS;

CREATE TABLE BETS (
    ID_BET INTEGER NOT NULL,
    BET VARCHAR2(10) NOT NULL,
    VALUE_BET NUMBER(*,2),
    ID_USER NUMBER(38,0) NOT NULL,
    ID_EVENT NUMBER(38,0) NOT NULL,
    
    CONSTRAINT PK_BETS_ID_BET PRIMARY KEY (ID_BET),
    CONSTRAINT FK_BETS_ID_USER FOREIGN KEY (ID_USER) REFERENCES ACCOUNTS (ID),
    CONSTRAINT FK_BETS_ID_EVENT FOREIGN KEY (ID_EVENT) REFERENCES EVENTS (ID_EVENT)
);


CREATE SEQUENCE SEQ_BETS START WITH 1 INCREMENT BY 1;
